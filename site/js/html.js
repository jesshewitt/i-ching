const escapeMap = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#39;'
}

const escape = v => v == null ? '' : String(v).replace(/[&<>"']/g, c => escapeMap[c])

class TrustedHTML {
    constructor(value) { this.value = value }
    toString() { return this.value }
}

const stringify = v => {
    if (v instanceof TrustedHTML) return v.value
    if (Array.isArray(v)) return v.map(stringify).join('')
    return escape(v)
}

export function html(strings, ...values) {
    let out = strings[0]
    for (let i = 0; i < values.length; i++) {
        out += stringify(values[i]) + strings[i + 1]
    }
    return new TrustedHTML(out)
}

// wrap a string that is already known-safe HTML (e.g. generated by other html`` calls or from trusted data)
export const raw = s => new TrustedHTML(s)
